#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*result;

	i = 0;
	while (i < n)
	{
		result = &s[i];
		*result = 0;
		i++;
	}
	return (s);
}
